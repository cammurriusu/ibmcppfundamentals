#include <iostream>
using namespace std;

int main()
{
    cout << "Size of Integer=" << sizeof(int) << " bytes";
    cout << "\nSize of Floating point=" << sizeof(float) << " bytes";
    cout << "\nSize of double=" << sizeof(double) << " bytes";
    cout << "\nSize of character=" << sizeof(char) << " byte";
    cout << "\nSize of Long integer=" << sizeof(long) << " bytes";
    cout << "\nSize of Short integer=" << sizeof(short) << " bytes";
}